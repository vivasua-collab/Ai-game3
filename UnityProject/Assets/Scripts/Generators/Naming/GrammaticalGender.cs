using System;

namespace CultivationGame.Generators
{
    /// <summary>
    /// Грамматический род в русском языке.
    /// Используется для согласования прилагательных с существительными.
    /// 
    /// Примеры согласования:
    /// - Masculine: "Пылающий меч" (мужской + мужской)
    /// - Feminine: "Пылающая секира" (женский + женский)
    /// - Neuter: "Пылающее копьё" (средний + средний)
    /// - Plural: "Пылающие перчатки" (мн.ч. + мн.ч.)
    /// </summary>
    public enum GrammaticalGender
    {
        /// <summary>
        /// Мужской род (меч, топор, посох, кинжал)
        /// Окончания: -∅, -й, -ь
        /// </summary>
        Masculine = 0,
        
        /// <summary>
        /// Женский род (секира, катана, мантия, броня)
        /// Окончания: -а, -я, -ь
        /// </summary>
        Feminine = 1,
        
        /// <summary>
        /// Средний род (копьё, кольцо, ожерелье)
        /// Окончания: -о, -е, -ё
        /// </summary>
        Neuter = 2,
        
        /// <summary>
        /// Множественное число (перчатки, сапоги, наручи)
        /// Окончания: -ы, -и, -а
        /// </summary>
        Plural = 3
    }
}
