#if UNITY_EDITOR
namespace CultivationGame.Editor.SceneBuilder
{
    /// <summary>
    /// Интерфейс фазы сборки сцены.
    /// Каждая фаза:
    ///   1. Проверяет, нужно ли действие (IsNeeded)
    ///   2. Выполняет (Execute)
    ///   3. Логирует результат
    ///
    /// Повторный запуск безопасен — пропускает уже выполненные фазы.
    /// </summary>
    public interface IScenePhase
    {
        /// <summary>Короткое имя фазы (для логирования)</summary>
        string Name { get; }

        /// <summary>Путь в меню (например "Phase 01: Folders")</summary>
        string MenuPath { get; }

        /// <summary>Порядковый номер фазы (1-15)</summary>
        int Order { get; }

        /// <summary>Проверяет, нужно ли выполнение фазы</summary>
        bool IsNeeded();

        /// <summary>Выполняет фазу</summary>
        void Execute();
    }
}
#endif
